  MimeTypes([Map<String, List<String>> map = const {}]) {
    map.forEach((mimeType, extensions) {
      _extensions[mimeType] = extensions;
      for (var extension in extensions) {
        _mimeTypes.putIfAbsent(extension, () => []).add(mimeType);
      }
    });

    registerGuesser(FileBinaryMimeTypeGuesser());
    registerGuesser(FileInfoMimeTypeGuesser());
  }

  static void setDefault(MimeTypes defaultType) {
    _default = defaultType;
  }

  static MimeTypes getDefault() {
    return _default ??= MimeTypes();
  }

  void registerGuesser(MimeTypeGuesserInterface guesser) {
    _guessers.add(guesser);
  }

  @override
  List<String> getExtensions(String mimeType) {
    List<String>? extensions;
    if (_extensions.isNotEmpty) {
      extensions = _extensions[mimeType] ?? _extensions[mimeType.toLowerCase()];
    }
    return extensions ?? MAP[mimeType] ?? MAP[mimeType.toLowerCase()] ?? [];
  }

  @override
  List<String> getMimeTypes(String ext) {
    List<String>? mimeTypes;
    if (_mimeTypes.isNotEmpty) {
      mimeTypes = _mimeTypes[ext] ?? _mimeTypes[ext.toLowerCase()];
    }
    return mimeTypes ?? reverseMap[ext] ?? reverseMap[ext.toLowerCase()] ?? [];
  }

  @override
  Future<String?> guessMimeType(String path) async {
    for (var guesser in _guessers.reversed) {
      if (!guesser.isGuesserSupported()) {
        continue;
      }
      String? mimeType = await guesser.guessMimeType(path);
      if (mimeType != null) {
        return mimeType;
      }
    }
    if (!isGuesserSupported()) {
      throw LogicException(
          'Unable to guess the MIME type as no guessers are available.');
    }
    return null;
  }

  @override
  bool isGuesserSupported() {
    for (var guesser in _guessers) {
      if (guesser.isGuesserSupported()) {
        return true;
      }
    }
    return false;
  }
